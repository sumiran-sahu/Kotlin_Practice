fun main (args:Array<String>){

    //FOR LOOP   here increment of 1 (one)
    for (i in 1..5){
        println("Hello "+i)
    }

    //increment of two
    for (i in 1.. 5 step 2){
        println("hello "+i)
    }

    //Decrement  of one, We can also add step here
    for (i in 5 downTo 1){
        print("hello"+i+" ")
    }



}